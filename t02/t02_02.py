
n = int(input("Введіть натуральне тризначне число: "))

d0 = n % 10
n = n // 10
d1 = n % 10
n = n // 10
d2 = n % 10

if d0 == 2 or d1 == 2 or d2 == 2:
    print("Число %i містить цифру 2" % n)

if d0 % 2 == 0 and d1 % 2 == 0 and d2 % 2 == 0:
    print("Число %i складається лише з парних цифр" % n)

if d0 + d1 + d2 == 18:
    print("Сума цифр числа %i дорівнює 18" % n)
